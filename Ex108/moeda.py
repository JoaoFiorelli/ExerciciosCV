def aumentar(n, porcentagem):
    aum = (1 + porcentagem/100) * n
    return aum


def diminuir(n, porcentagem):
    dim = (1 - porcentagem/100) * n
    return dim


def dobro(n):
    dob = 2 * n
    return dob


def metade(n):
    met = n / 2
    return met


def moeda(n, tipo = "R$"):
    return f"{tipo}{n:.2f}".replace(".",",")
